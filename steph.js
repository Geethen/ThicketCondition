// ==========================================================
// RF PROBABILITY FUNCTION
// ==========================================================
function runRFProb(modelName, trainCollection, predictionMask) {

  if (verbose) {
    print(modelName + ' training count', trainCollection.size());
    print(modelName + ' class histogram', trainCollection.aggregate_histogram('Class'));
  }

  var trainSamp = emb.sampleRegions({
    collection: trainCollection,
    properties: ['Class', 'ClassId', 'efg_id', 'EFG', 'lon', 'lat'],
    scale: scale,
    geometries: false,
    tileScale: 4
  });

  var rfProb = ee.Classifier.smileRandomForest({
    numberOfTrees: 300,
    seed: 123,
    minLeafPopulation: 1,
    bagFraction: 0.632
  })
  .setOutputMode('MULTIPROBABILITY')
  .train({
    features: trainSamp,
    classProperty: 'ClassId',
    inputProperties: predictors
  });

  var naturalPredictionMask = predictionMask
    .updateMask(naturalMask)
    .updateMask(notWaterMask);

  var probArray = emb
    .updateMask(naturalPredictionMask)
    .classify(rfProb)
    .select(0);

  var p_intact = probArray.arrayGet([0]).rename('p_intact').toFloat();
  var p_moderate = probArray.arrayGet([1]).rename('p_moderate').toFloat();
  var p_severe = probArray.arrayGet([2]).rename('p_severe').toFloat();

  // Set transformed pixels to -1 in all probability bands
  var transformedInEFG = transformedMask.updateMask(predictionMask);
  var transformedValue = ee.Image.constant(-1).toFloat();

  var validMask = naturalPredictionMask.unmask(0)
    .or(transformedInEFG.unmask(0));

  p_intact = p_intact.unmask(transformedValue)
    .updateMask(validMask)
    .rename('p_intact')
    .toFloat();

  p_moderate = p_moderate.unmask(transformedValue)
    .updateMask(validMask)
    .rename('p_moderate')
    .toFloat();

  p_severe = p_severe.unmask(transformedValue)
    .updateMask(validMask)
    .rename('p_severe')
    .toFloat();

  var probStack = ee.Image.cat([
    p_intact,
    p_moderate,
    p_severe
  ]);

  return probStack;
}

